#include "so_long.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int n)
{
	if (n == 0)
		ft_putchar('0');
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + 48);
}

void	print_moves(int move)
{
	ft_putstr("Moves: ");
	ft_putnbr(move);
	ft_putstr("\n");
}
